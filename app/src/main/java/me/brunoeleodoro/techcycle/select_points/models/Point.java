package me.brunoeleodoro.techcycle.select_points.models;

public class Point {

    private String endereco;
    private String sub;
    private String full_endereco;

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getSub() {
        return sub;
    }

    public void setSub(String sub) {
        this.sub = sub;
    }

    public String getFull_endereco() {
        return full_endereco;
    }

    public void setFull_endereco(String full_endereco) {
        this.full_endereco = full_endereco;
    }
}
